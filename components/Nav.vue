<template>
<div>
    <div class="bg-black fixed min-h-screen w-screen flex md:hidden overflow-hidden" ref="m_screen_menu">
        <div class="md:hidden flex absolute  right-10"><h3 ref="m_menu_button_close" @click="close"><p class="text-red-800">Zamkij</p></h3></div>
        <div class="md:hidden flex absolute left-10" ref="m_menu_button_close" @click="close"><p class="text-red-800">LOGO</p></div>
            <div class="absolute flex flex-col mb-24 gap-4 inset-10 justify-end">
                    <NuxtLink to=""><h3 class="text-white" ref="m_page1" @click="close">Page1</h3></NuxtLink>
                    <NuxtLink to=""><h3 class="text-white" ref="m_page2" @click="close">Page1</h3></NuxtLink>
                    <NuxtLink to=""><h3 class="text-white" ref="m_page3" @click="close">Page1</h3></NuxtLink>
                    <NuxtLink to=""><h3 class="text-white" ref="m_page4" @click="close">Page1</h3></NuxtLink>
    </div>
    </div>
    <div class="columns-2 w-full mx-auto px-10 flex py-6">
        <div class=" z-100 text-2xl font-medium text-black" ref="logo">LOGO</div>
        <div class="md:hidden flex w-full justify-end"><h3 ref="m_menu_button_open" @click="open" ><p class="text-black">Menu</p></h3></div>
        <div class="columns-4 w-full md:flex justify-end gap-12 hidden">
            <NuxtLink to=""><h3>Page1</h3></NuxtLink>
            <NuxtLink to=""><h3>Page1</h3></NuxtLink>
            <NuxtLink to=""><h3>Page1</h3></NuxtLink>
            <NuxtLink to=""><h3>Page1</h3></NuxtLink>
        </div>
    </div>
</div>
</template>

<script setup lang="ts">
import {ref, onMounted} from 'vue';
import gsap from 'gsap';

const m_screen_menu = ref();
const m_menu_button_close = ref();
const m_menu_button_open = ref();
const m_page1 = ref();
const m_page2 = ref();
const m_page3 = ref();
const m_page4 = ref();

if (process.client) {
  var m_header_open_animation = gsap.timeline({paused: true})
}

    onMounted(()=>{

            m_header_open_animation.from(m_screen_menu.value,{
                            duration: 0.6,
                            opacity: 0,
                            display: "none",
                            })
                            .from(m_menu_button_open.value,{
                            duration: 0.6,
                            },"<+=0.12")
                            .from(m_page1.value,{
                            duration: 0.6,
                            opacity: 0,
                            },"<+=0.12")
                            .from(m_page2.value,{
                            duration: 0.6,
                            opacity: 0,
                            },"<+=0.12")
                            .from(m_page3.value,{
                            duration: 0.6,
                            opacity: 0,
                            },"<+=0.12")
                            .from(m_page4.value,{
                            duration: 0.6,
                            opacity: 0,
                            },"<+=0.12")

})

const open = () => { m_header_open_animation.play();
    document.body.style.overflow = "hidden";
    document.body.style.userSelect = "none";
  }
const close = () => { m_header_open_animation.reverse();
    document.body.style.overflow = "auto";
    document.body.style.userSelect = "auto"; 
 }

</script>

